using System;

namespace Day07
{
    class Calculator
    {
        // 1. Add two integers
        public int Sum(int a, int b)
        {
            return a + b;
        }

        // 2. Add three integers
        public int Sum(int a, int b, int c)
        {
            return a + b + c;
        }

        // 3. Add two doubles
        public double Sum(double a, double b)
        {
            return a + b;
        }
    }
}